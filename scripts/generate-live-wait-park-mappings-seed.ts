/**
 * Generates supabase/seeds/live_wait_park_mappings.sql from Queue-Times parks.json
 * plus name matching against TripTiles `parks` rows (scraped from migration SQL).
 *
 * Run: npx tsx scripts/generate-live-wait-park-mappings-seed.ts
 */

import { readdirSync, readFileSync, writeFileSync } from "node:fs";
